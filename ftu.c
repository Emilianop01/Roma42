#include "ft_printf.h"

int	ftu(unsigned int i)
{
	int	cont;
	int	res;

	cont = 0;
	if (i > 9)
		ftu(i / 10);
	res = i % 10 + '0';
	write(1, &res, 1);
	cont++;
	return (cont);
}
