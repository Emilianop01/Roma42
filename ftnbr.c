#include "ft_printf.h"

int	ftnbr(int i)
{
	int	cont;
	int	res;

	cont = 0;
	if (i == -2147483648)
	{
		write(1, "-2147483648", 11);
		return (11);
	}
	if (i < 0)
	{
		write(1, "-", 1);
		i = -i;
		cont++;
	}
	if (i > 9)
		ftnbr(i / 10);
	res = i % 10 + '0';
	write(1, &res, 1);
	cont++;
	return (cont);
}
